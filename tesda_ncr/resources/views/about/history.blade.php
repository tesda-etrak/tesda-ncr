        <p class="my-8">
            The Technical Education and Skills Development Authority (TESDA) was established through the enactment of <strong>Republic Act No. 7796</strong> 
            otherwise known as the "Technical Education and Skills Development Act of 1994", which was signed into law by President Fidel V. Ramos on August 25, 1994. 
            This Act aims to encourage the full participation of and mobilize the industry, labor, local government units and technical-vocational institutions 
            in the skills development of the country's human resources.
        </p>
        <p class="my-8">
            The merging of the <strong>National Manpower and Youth Council</strong> (NMYC) of the Department of Labor and Employment (DOLE). 
            <strong>The Bureau of Technical and Vocational Education</strong> (BTVE) of the <strong>Department of Education, Culture and Sports</strong> (DECS), and 
            The Apprenticeship Program of the <strong>Bureau of Local Employment</strong> (BLE) of the DOLE gave birth to TESDA.
        </p>
        <p class="my-8">
            The fusion of the above offices was one of the key recommendations of the 1991 Report of the Congressional Commission on Education, 
            which undertook a national review of the state of Philippine education and manpower development. It was meant to reduce overlapping in skills development 
            activities initiated by various public and private sector agencies, and to provide national directions for the country's technical-vocational education 
            and training (TVET) system. Hence, a major thrust of TESDA is the formulation of a comprehensive development plan for middle-level manpower based on the 
            National Technical Education and Skills Development Plan. This plan shall provide for a reformed industry-based training program that includes 
            apprenticeship, dual training system and other similar schemes.
        </p>
        <div class="sm:flex sm:items-center sm:justify-center sm:my-16 sm:space-x-5 my-8 space-y-6">
            <div class="border border-gray-300 p-6 shadow-md w-full">
                <h3 class="text-blue-800 text-2xl font-semibold mb-1">TESDA is mandated to: </h3>
                <ul class="list-decimal ml-14">
                    <li>Integrate, coordinate and monitor skills development programs;</li>
                    <li>Restructure efforts to promote and develop middle-level manpower;</li>
                    <li>Approve skills standards and tests;</li>
                    <li>Develop an accreditation system for institutions involved in middle-level manpower development;</li>
                    <li>Fund programs and projects for technical education and skills development; and</li>
                    <li>Assist trainers training programs.</li>
                </ul>
            </div>
            <div class="border border-gray-300 p-6 shadow-md w-full">
                <h3 class="text-blue-800 text-2xl font-semibold mb-1">At the same time, TESDA is expected to: </h3>
                <ul class="list-decimal ml-14">
                    <li>Devolve training functions to local governments;</li>
                    <li>Reform the apprenticeship program;</li>
                    <li>Involve industry/employers in skills training;</li>
                    <li>Formulate a skills development plan;</li>
                    <li>Develop and administer training incentives;</li>
                    <li>Organize skills competitions;; and</li>
                    <li>Manage skills development funds.</li>
                </ul>
            </div>
        </div>
        <p class="my-8">
            Overall, TESDA formulates manpower and skills plans, sets appropriate skills standards and tests, coordinates and monitors manpower policies and programs, 
            and provides policy directions and guidelines for resource allocation for the TVET institutions in both the private and public sectors.
        </p>
        <p class="my-8">
            Today, TESDA has evolved into an organization that is responsive, effective and efficient in delivering myriad services to its clients. 
            To accomplish its multi-pronged mission, the TESDA Board has been formulating strategies and programs geared towards yielding the highest impact on 
            manpower development in various areas, industry sectors and institutions.
        </p>
