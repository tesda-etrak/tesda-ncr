                <p class="text-blue-800 font-medium">NTESDP 2023-2028 Framework and Strategic Pillars</p>
                <div>
                    <p class="text-blue-800 font-medium">STRATEGIC FRAMEWORK</p>
                    <div class="space-y-5">
                        <img src="{{ asset('images/national-tesd-plan/NTESDP2023-2028-4x.png') }}" alt="National TESD Plan 2023-2028 (4x)" class="float-left mr-4 w-fit h-auto">
                        <p>
                            The NTESDP aims for Philippine TVET shall acquire the same level and respect and recognition given to other TVET systems in the 
                            other parts of the world and shall serve as a primary platform for lifelong learning that can be accessed by every Filipino in search of 
                            professional advancement and continuing education.
                        </p>
                        <p>
                            The mission of the TVET sector shall be "To promote, provide, and support accessible, inclusive, equitable, relevant, and high quality TVET". 
                            The success of the mission will be demonstrated by the impact of interventions such as: (a) increased number of Filipinos exhibiting 
                            public acceptance and recognition of the value of TVET; (b) increased number of Filipinos from marginalized groups or communities provided with 
                            TVET opportunities; (c) increased number of TVET programs that address changing work needs; and (d) increased number of foreign employers 
                            that recognized and accepted Philippine TVET graduates with their certifications and qualifications.
                        </p>
                    </div>
                </div>
                <div id="sixStrategicPillars" class="space-y-5">
                    <p>The NTESDP has six (6) Strategic Pillars and all the strategies shall be implemented with the Area-based Demand-driven TVET as the central strategy.</p>
                    <p class="text-blue-800 font-medium">OUTCOME AND STRATEGIES</p>
                    <div id="pillar-1" class="space-y-5">
                        <p class="text-blue-800 font-medium">Strategic Pillar 1: Modern and responsive technical and vocational training and lifelong learning advanced and reinforced</p>
                        <p>
                            Given the urgency to address new relationships between education, the changing world of work, evolving needs of industries and communities in 
                            the era of the Fourth Industrial Revolution, the NTESDP 2023-2028 shall aggressively pursue the advancement of 
                            technical and vocational training and lifelong learning. It shall lead in the creation of open and flexible educational structures, 
                            recognizing work experience as a part of learning. It shall create a learning culture that permits individuals to expand their 
                            intellectual horizons, and provide opportunities and access to all learners, especially among members of marginalized communities, 
                            to acquire and to constantly improve professional skills and knowledge, and to engage positively in society to utilize 
                            the fruits of economic and technological change.
                        </p>
                        <p>To bring about this outcomes, the NTESDP will pursue the following strategies:</p>
                        <ul class="list-disc list-inside">
                            <li>Design national and regional workforce and skills roadmaps and plans</li>
                            <li>Innovate pedagogy, skilling, upskilling, re-skilling and assessments of TVET providers and incentivize high-performing, quality training institutions</li>
                            <li>Drive urban and rural industry development and strengthen community and area-based, enterprise-based, and demand-driven future-ready TVET programs</li>
                            <li>Design sector-specific programs that address the needs of industries in identified key employment generators, emerging sectors, including entrepreneurship in the green and blue economy, the Fourth Industrial Revolution, and the Future of Work</li>
                            <li>Strengthen the capacity of national and local training systems to deliver workforce readiness and technical-vocational training for vulnerable out-of-school youth and people in marginalized situations</li>
                        </ul>
                    </div>
                    <div id="pillar-2" class="space-y-5">
                        <p class="text-blue-800 font-medium">Strategic Pillar 2: Quality and productivity of workforce enhanced</p>
                        <p>
                            The NTESDP 2023-2028 shall work for full recognition, appreciation, and employability of TVET learners and graduates, allowing them to receive 
                            better employment options and higher salaries and improve their career mobility. It shall provide platforms and mechanisms of learning that 
                            address the needs for higher level of competencies and skills, especially in the various high value-added, high-tech sectors where there is 
                            utilization of a higher level of technology, operations and work processes. It shall broaden access to quality TVET, especially targeting youth, 
                            MSMEs, cooperatives, women, PWDs, IP communities, and groups in marginalized situations for gender equality and social inclusion.
                        </p>
                        <p>The specific strategies are the following:</p>
                        <ul class="list-disc list-inside">
                            <li>Ensure TVET opportunities are available to all who want to access them, and ensure equality of access across the system</li>
                            <li>Widen learning and educational opportunities for the youth, MSMEs, cooperatives, women, PWDs, IP communities, and groups in marginalized situations for gender equality and social inclusion</li>
                            <li>Increase the number of youth and adults with entrepreneurship skills</li>
                            <li>Work with DOLE and industry boards on programs geared towards increasing employability and wages for TVET graduates</li>
                        </ul>
                    </div>
                    <div id="pillar-3" class="space-y-5">
                        <p class="text-blue-800 font-medium">Strategic Pillar 3: Quality assurance on certifications, standards, equivalencies, and qualifications strengthened</p>
                        <p>
                            The NTESDP 2023-2028 shall be committed to quality assurance as a fundamental anchor for the comparability and recognition of qualifications of 
                            Philippine TVET at both the national and international levels. It shall ensure the validity, consistency, and trustworthiness of 
                            Philippine qualifications and certificates through reliable and standardized quality assurance arrangements and harmonized rating systems and 
                            mechanisms. It shall work on improving stakeholders’ confidence and trust on the assessment and qualification processes and that 
                            the certifications gained by TVET graduates valid and reliable testaments to their knowledge, skills and wider competences.
                        </p>
                        <p>Specifically, the NTESDP will:</p>
                        <ul class="list-disc list-inside">
                            <li>Improve national and regional qualification frameworks to increase portability of qualifications and develop progression pathways for learner and labor mobility</li>
                            <li>Increase awareness and accelerate the operationalization / implementation of the Philippine Qualification Framework (PQF), and integrate integrating Philippine Skills Framework (PSF) competencies in TVET courses</li>
                            <li>Ensure clear pathway for TVET towards lifelong learning, higher education, and regional mobility (including Strengthened Technical Vocational Education Program (STVEP) and SHS Technical-Vocational-Livelihood (TVL) Track)</li>
                            <li>Pursue TVET internationalization by benchmarking skill standards, competency assessment and certification with international standards and referencing</li>
                            <li>Operationalize the Philippine Credit Transfer System to optimize learner-to-worker mobility, higher TVET qualifications, and TESDA's distinctive area of competence</li>
                        </ul>
                    </div>
                    <div id="pillar-4" class="space-y-5">
                        <p class="text-blue-800 font-medium">Strategic Pillar 4: Collaborations with industry, government, academe, labor, and other stakeholders expanded and sustained</p>
                        <p>
                            The Plan shall strengthen partnerships with industry, local government units, universities and colleges, employer associations as well as 
                            other community stakeholders. It shall foster regular and active communication between TVET partners and TVET providers and provide enabling 
                            mechanisms to share data and insights relevant to curriculum and program development, skills framework development, emerging jobs, and 
                            job reskilling and upskilling.
                        </p>
                        <p>To bring about these collaborations with other TVET stakeholders, the NTESDP will pursue the following strategies:</p>
                        <ul class="list-disc list-inside">
                            <li>Increase participation and ownership by employers and other stakeholders for more relevant and responsive enterprise-based trainings and apprenticeship programs</li>
                            <li>Establish seamless coordination and relationship with DepED, CHED, TVIs, NGAs, HEIs (including SUCs and LUCs) for TVET recognition in the credit transfer system</li>
                            <li>Manage autonomy for public training providers and strengthen support provision to private training providers</li>
                            <li>Develop capacity building programs for Public and Private TVET Training Institution Trainers</li>
                            <li>Establish physical platforms for collaborations on learning, innovation, and entrepreneurship such as the TVET Innovation Centers, Maker's Space, and Incubation Hubs</li>
                            <li>Strongly campaign for public understanding, appreciation, high regard, and support of TVET</li>
                            <li>Progressively devolve TVET community-based training and institutionalization of Community Training and Employment Coordinators (CTECs)</li>
                            <li>To ensure linkages of training to employment platform of DOLE to attain decent, sustainable, and quality jobs and livelihoods</li>
                        </ul>
                    </div>
                    <div id="pillar-5" class="space-y-5">
                        <p class="text-blue-800 font-medium">Strategic Pillar 5: Transformative and innovative TVET ecosystem organized and strengthened</p>
                        <p>
                            We shall strengthen the interconnectedness of the factors that are crucial to the operations of the Philippine TVET ecosystem such as 
                            learner information management system, research and development, monitoring and evaluation, communication and promotion, social inclusion, 
                            and sustainable practices.
                        </p>
                        <p>The NTESDP will pursue the following:</p>
                        <ul class="list-disc list-inside">
                            <li>Establish inter-operability of labor market information systems and management information systems on learners/workers as well as current, critical, and emerging skills and jobs</li>
                            <li>Nurture a strong culture of research and innovation to generate knowledge and new policies, products, systems, and processes for efficient TVET delivery and optimal learning</li>
                            <li>Establish a robust mechanism for monitoring and evaluation</li>
                            <li>Instill and communicate value of TVET culture</li>
                        </ul>
                    </div>
                    <div id="pillar-6" class="space-y-5">
                        <p class="text-blue-800 font-medium">Strategic Pillar 6: Philippine TVET System and Governance Harmonized</p>
                        <p>
                            Finally, we shall strongly and widely promote TVET’s pivotal role in lifelong learning and its significant position in the 
                            Philippine education sector. It shall advocate and campaign for increased financing and support from government as well as investments and 
                            participation from the private sector. It shall ensure that the TVET culture and its implementation is felt at the grassroots via formal and 
                            informal mechanisms and structures.
                        </p>
                        <p>The Plan shall pursue the following strategies:</p>
                        <ul class="list-disc list-inside">
                            <li>Instill and deepen organizational culture and identity of TVET as a sector</li>
                            <li>Advocate for increased public investments, sustainable, and expanded TVET financing</li>
                            <li>Develop and promote a national policy for lifelong learning</li>
                            <li>Strengthen the functionalities of Regional/Provincial TESD Committees</li>
                            <li>Harmonize the tri-focalized system of education for lifelong learning and streamline employment, livelihood, and support programs of government</li>
                        </ul>
                    </div>
