                <p>
                    The NTESDP 2023-2028 is anchored on the vision of A Globally Recognized Philippine TVET as a Catalyst for Education and Lifelong Learning, Workforce, 
                    and Socio-Economic Transformation, with Area-based Demand-driven TVET as its central strategy. This overarching vision aligns with the objectives of 
                    Philippine Development Plan (PDP), especially Chapter 2.2. Transformative Lifelong Learning Opportunities for all Ensured and 
                    Chapter 4 Income-Earning Ability Increased. It is also aligned with the Philippine Labor and Employment Plan 2023-2028, other sectoral plans, 
                    national directives, legislations, and international policies.
                </p>
                <p>
                    The National Technical Education and Skills Development Plan (NTESDP) 2023-2028 aims to provide access to high-quality opportunities for 
                    lifelong learning, as well as to provide Filipinos with smart and innovative skills. The plan focuses on creating future-ready TVET programs, 
                    advancing ladderized education, and pursuing collaborations in recognizing relevant courses offered by NGAs, technical-vocational institutions and 
                    HEIs (including state and local universities and colleges). It also aims to increase enterprise-based training (EBT), particularly emphasizing 
                    dual training systems, apprenticeships, and learnerships, and recognizing relevant courses offered by NGAs, technical-vocational institutions and 
                    HEIs (including state and local universities and colleges).
                </p>
                <p>
                    Philippine TVET has to proactively address the urgency for constant skilling, reskilling, and upskilling while cultivating the critical thinking, 
                    communications, collaboration, adaptability, futures thinking, and higher-order cognitive skills of the country’s workforce and entrepreneurs. 
                    Lifelong learning as a primordial philosophical anchor of Philippine TVET summons a level of quality and availability of training and skills as well as 
                    mindset that will make an important difference in employment outcomes of individuals. At the country level, lifelong learning calls for crafting 
                    new skills policy and recalibrated TVET directions and programs to help shape workforce readiness and national competitiveness amidst 
                    global economic developments and the Fourth Industrial Revolution.
                </p>
                <p>
                    At the same time, Philippine TVET has to reinforce its foundations as the country’s primary vehicle of access, inclusivity, and mobility that provides 
                    hope and practical economic opportunities for those who are hampered by poverty, geographical and cultural isolation, physical disadvantages, and 
                    other challenges. It has to lead in providing the platforms needed to enable the country’s workforce to transition in the green, digital, and 
                    knowledge economies and to move across industries and occupations, along career paths that may be nonlinear. It has to champion the flexible pathways 
                    between different levels of formal education and for the recognition and development of skills acquired outside formal education.
                </p>
